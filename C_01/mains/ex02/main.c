#include <unistd.h>

void	ft_swap(int *a, int*b);

int	main(void)
{
	int	a;
	int	b;

	a = 74;
	b = 77;
	write(1, &a, 1);
	write(1, &b, 1);
	ft_swap(&a, &b);
	write(1, &a, 1);
	write(1, &b, 1);
	return (0);
}
