int	m_initialize(int m[][6])
{
	int	i;
	int	j;

	i = 0;
	while (i < 6)
	{
		j = 0;
		while (j < 6)
		{
			m[i][j] = 0;
			j++;
		}
		i++;
	}
	return (0);
}
