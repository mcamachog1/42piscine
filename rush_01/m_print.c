#include <stdio.h>

int	m_print (int m[][6])
{
	int     i;
	int     j;

	i = 0;
	while (i < 6)
	{
                j = 0;
                while (j < 6)
                {
                        printf("%d", m[i][j]);
                        j++;
                }
                printf("\n");
                i++;
        }
        return 0;
}
