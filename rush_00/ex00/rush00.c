void	ft_putchar(char c);

void	rush(int x, int y)
{
	int	c;
	int	r;

	if (x == 0 || y == 0)
		return ;
	r = 0;
	while (r < y)
	{
		c = 0;
		while (c < x)
		{
			if ((c == 0 || c == x - 1) && (r == 0 || r == y - 1))
				ft_putchar('o');
			if ((c == 0 || c == x - 1) && !(r == 0 || r == y - 1))
				ft_putchar('|');
			if (c != 0 && c != x - 1 && r != 0 && r != y - 1)
				ft_putchar(' ');
			if ((r == 0 || r == y - 1) && c != 0 && c != x - 1)
				ft_putchar('-');
			c++;
		}
		r++;
		ft_putchar('\n');
	}
}
